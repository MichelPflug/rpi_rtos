static const unsigned char glyph_box[8] = {0x00,0x7E,0x42,0x42,0x42,0x42,0x7E,0x00};

static const unsigned char *glyph_for(char c)
{
    unsigned uc = (unsigned char)c;
    if (uc >= 0x20 && uc <= 0x7E) { return font8x8[uc - 0x20]; }
    return glyph_box;                                    /* unbekannt -> Kasten */
}

int gui_init(gui_t *g)
{
    gui_fb_info_t info;
    if (sys3(SYS_GUI_INFO, (long)&info, 0, 0) != 0) { return -1; }
    g->bb     = (volatile unsigned *)(unsigned long)info.fb_va;
    g->width  = info.width;
    g->height = info.height;
    g->wpr    = info.pitch / 4;
    return 0;
}

void gui_flush(const gui_t *g, int y, int nrows)
{
    if (nrows <= 0) { return; }
    if (y < 0) { nrows += y; y = 0; }
    if (nrows <= 0) { return; }
    /* symmetrisch zu gui_fill_rect auch oben gegen die Hoehe klemmen (der Kernel klemmt zwar auch). */
    if ((unsigned)y >= g->height) { return; }
    if ((unsigned)nrows > g->height - (unsigned)y) { nrows = (int)(g->height - (unsigned)y); }
    sys3(SYS_GUI_FLUSH, y, nrows, 0);
}

void gui_flush_all(const gui_t *g) { sys3(SYS_GUI_FLUSH, 0, (long)g->height, 0); }

void gui_pixel(const gui_t *g, int x, int y, unsigned rgb)
{
    if (x < 0 || y < 0 || (unsigned)x >= g->width || (unsigned)y >= g->height) { return; }
    g->bb[(unsigned)y * g->wpr + (unsigned)x] = rgb;
}

unsigned gui_get(const gui_t *g, int x, int y)
{
    if (x < 0 || y < 0 || (unsigned)x >= g->width || (unsigned)y >= g->height) { return 0; }
    return g->bb[(unsigned)y * g->wpr + (unsigned)x];
}

void gui_fill_rect(const gui_t *g, int x, int y, int w, int h, unsigned rgb)
{
    if (w <= 0 || h <= 0) { return; }
    if (x < 0) { w += x; x = 0; }                        /* links clippen (w+x <= w, kein Overflow) */
    if (y < 0) { h += y; y = 0; }
    if (w <= 0 || h <= 0) { return; }
    /* x,y >= 0. Rechts/unten UNSIGNED klemmen -> kein signed-Overflow von x+w / y+h bei extremen
     * (near-INT_MAX) Eingaben; danach gilt x+w <= width, y+h <= height (beide klein). */
    if ((unsigned)x >= g->width || (unsigned)y >= g->height) { return; }
    if ((unsigned)w > g->width  - (unsigned)x) { w = (int)(g->width  - (unsigned)x); }
    if ((unsigned)h > g->height - (unsigned)y) { h = (int)(g->height - (unsigned)y); }
    for (int yy = y; yy < y + h; yy++) {
        volatile unsigned *row = g->bb + (unsigned)yy * g->wpr;
        for (int xx = x; xx < x + w; xx++) { row[xx] = rgb; }
    }
}

void gui_hline(const gui_t *g, int x, int y, int len, unsigned rgb) { gui_fill_rect(g, x, y, len, 1, rgb); }
void gui_vline(const gui_t *g, int x, int y, int len, unsigned rgb) { gui_fill_rect(g, x, y, 1, len, rgb); }

void gui_rect(const gui_t *g, int x, int y, int w, int h, unsigned rgb)
{
    if (w <= 0 || h <= 0) { return; }
    gui_hline(g, x, y, w, rgb);
    gui_hline(g, x, y + h - 1, w, rgb);
    gui_vline(g, x, y, h, rgb);
    gui_vline(g, x + w - 1, y, h, rgb);
}

void gui_clear(const gui_t *g, unsigned rgb) { gui_fill_rect(g, 0, 0, (int)g->width, (int)g->height, rgb); }

void gui_char(const gui_t *g, int x, int y, char c, unsigned fg, unsigned bg, int scale)
{
    if (scale < 1) { scale = 1; }
    if (scale > 256) { scale = 256; }                    /* col*scale bleibt klein -> kein Overflow */
    const unsigned char *bm = glyph_for(c);
    for (int row = 0; row < GUI_GLYPH_H; row++) {
        unsigned char bits = bm[row];
        for (int col = 0; col < GUI_GLYPH_W; col++) {
            unsigned color = (bits & (0x80u >> col)) ? fg : bg;
            if (color == GUI_TRANSPARENT) { continue; }
            gui_fill_rect(g, x + col * scale, y + row * scale, scale, scale, color);
        }
    }
}

void gui_text(const gui_t *g, int x, int y, const char *s, unsigned fg, unsigned bg, int scale)
{
    if (scale < 1) { scale = 1; }
    int x0 = x, adv = GUI_GLYPH_W * scale;
    for (; *s; ++s) {
        if (*s == '\n') { y += GUI_GLYPH_H * scale; x = x0; continue; }
        gui_char(g, x, y, *s, fg, bg, scale);
        x += adv;
    }
}

/* --- Proportionale TTF-Bitmap-Fonts (.rfn, siehe tools/gen_font.py) --- */
static int      rfn16(const unsigned char *p) { return (int)(short)(unsigned short)(p[0] | (p[1] << 8)); }
static unsigned rfn16u(const unsigned char *p) { return (unsigned)(p[0] | (p[1] << 8)); }
static unsigned rfn32(const unsigned char *p) { return (unsigned)(p[0] | (p[1] << 8) | (p[2] << 16) | ((unsigned)p[3] << 24)); }

int gui_font_from_mem(gui_font_t *font, const unsigned char *data, unsigned len)
{
    if (!font || !data || len < 12u) { return -1; }
    if (data[0] != 'R' || data[1] != 'F' || data[2] != 'N' || data[3] != '1') { return -1; }
    font->data        = data;
    font->line_height = (int)rfn16u(data + 4);
    font->ascent      = (int)rfn16u(data + 6);
    font->first       = data[8];
    font->count       = data[9];
    font->table       = data + 12;
    if (12u + (unsigned)font->count * 16u > len) { return -1; }
    return 0;
}

int gui_text_ttf_width(const gui_font_t *font, const char *s)
{
    if (!font || !s) { return 0; }
    int w = 0;
    for (; *s; ++s) {
        int cp = (unsigned char)*s;
        if (cp < font->first || cp >= font->first + font->count) { continue; }
        w += rfn16(font->table + (cp - font->first) * 16);   /* advance */
    }
    return w;
}

/* fg ueber bg mit Deckung a (0..255) mischen -- rein integer (EL0 hat kein FP). */
static unsigned rfn_blend(unsigned fg, unsigned bg, int a)
{
    int ia = 255 - a;
    int r = ((int)((fg >> 16) & 0xFF) * a + (int)((bg >> 16) & 0xFF) * ia + 127) / 255;
    int gc = ((int)((fg >> 8) & 0xFF) * a + (int)((bg >> 8) & 0xFF) * ia + 127) / 255;
    int b = ((int)(fg & 0xFF) * a + (int)(bg & 0xFF) * ia + 127) / 255;
    return ((unsigned)r << 16) | ((unsigned)gc << 8) | (unsigned)b;
}

void gui_text_ttf(const gui_t *g, int x, int y, const char *s, const gui_font_t *font,
                  unsigned fg, unsigned bg, int clip_right)
{
    if (!g || !font || !s) { return; }
    int pen = x;
    for (; *s; ++s) {
        int cp = (unsigned char)*s;
        if (cp < font->first || cp >= font->first + font->count) { continue; }
        const unsigned char *gl = font->table + (cp - font->first) * 16;
        int adv = rfn16(gl), gw = rfn16(gl + 2), gh = rfn16(gl + 4);
        int left = rfn16(gl + 6), ytop = rfn16(gl + 8);
        if (pen + left >= clip_right) { break; }               /* rechts abgeschnitten */
        const unsigned char *bmp = font->data + rfn32(gl + 12);
        for (int gy = 0; gy < gh; ++gy) {
            for (int gx = 0; gx < gw; ++gx) {
                int a = bmp[gy * gw + gx];
                if (a == 0) { continue; }
                int px = pen + left + gx;
                if (px >= clip_right) { break; }
                gui_pixel(g, px, y + ytop + gy, (a >= 255) ? fg : rfn_blend(fg, bg, a));
            }
        }
        pen += adv;
    }
}
